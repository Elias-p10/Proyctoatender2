    public void Encolar_Estudiates(){
     //un arreglo de tipo string que va a contener 4 elementos
     String [] registros = new String[4];
     
     
     String SQL="select * from Estudiantes";
     
     try {
         
         Statement st=con.createStatement();
         ResultSet rs=st.executeQuery(SQL);
         
         while (rs.next()) {         //lee cada linea de cada base de datos    
             
             registros[0]=rs.getString("estudiante");
             registros[1]=rs.getString("nombre");
             registros[2]=rs.getString("apellido");
             registros[3]=rs.getString("carnet");
             //
             alumno = new Estudiante(registros[1], registros[2], registros[3]);
             inscritos.enqueue(alumno);//encolamos alumno
             
         }
         
         LimpiarBD();//limpiar base de datos
         
         //por si falla
     } catch (SQLException e) {

          
          System.out.println("Error" + e.getMessage());
     }
 
 }
