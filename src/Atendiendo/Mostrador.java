    protected void Inscribiendo() {
        //hacer dos cosas al mismo tiempo
        SwingWorker<Void, Integer> worker = new SwingWorker<Void, Integer>() {
            int i = 0;
            @Override 
            protected Void doInBackground() throws Exception {
                while (!inscritos.isEmpty()) {
                    
                    alumno = inscritos.dequeue();// desencola el primero,segundo hasta que este vacio
                    
                    publish(i++);
                    
                    Thread.sleep(10000);//cada 10s desencola un nuevo alumno
                }
                
                return null;
            }
//funcion process
            protected void process(java.util.List<Integer> chunks) {
                atender();
            }

            @Override
            protected void done() {
               
               Encolar_Estudiates();
               Inscribiendo(); //es una funcion llamada al mismo es un ciclo que no va a salir
            }
        };
        worker.execute();
    }
    
